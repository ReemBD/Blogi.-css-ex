function onInit() {
    document.querySelectorAll('')
}

function toggleMenu() {
    document.body.classList.toggle('open-menu');
}

function toggleModal() {
    document.body.classList.toggle('open-modal');
}

function onHoverJournal() {
    document.querySelector('.journal').classList.add('open-dropdown');
}

function onHoverOutJournal() {
    document.querySelector('.journal').classList.remove('open-dropdown');
}